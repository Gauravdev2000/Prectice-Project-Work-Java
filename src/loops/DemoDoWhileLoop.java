package loops;

public class DemoDoWhileLoop {
    public static void main(String[] args) {
        //syntax ;
        //do{
        // statement}
        //while(condition);
        int i=1;
        do{
            System.out.println("This is Do while loop demo" +i);
            i++;
        } while(i<5);
        int j=5;
        do{
            System.out.println("This id do while decrement loop" +j);
            j--;
        }while(j>0);



    }
}
