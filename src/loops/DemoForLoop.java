package loops;

public class DemoForLoop {
    //SYNTAX : for(initialization, condition, increment,){
    //         //block of statement
    // }

    public static void main(String[] args) {
        for(int i= 1; i<10; i++){
            System.out.println("Iam Gaurav Dev" +i);

        }
        for(int i= 20; i>0; i--){
            System.out.println("Decrement Loops" +i);
        }



    }
}
