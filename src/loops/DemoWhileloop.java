package loops;

public class DemoWhileloop {
    public static void main(String[] args) {
        int i=1;
        while(i<10){
            System.out.println("While loop" +i);
            i++;
        }
        int j=2;
        while(j>0){
            System.out.println("decrement loop" +j);
            j--;
        }



    }
}
